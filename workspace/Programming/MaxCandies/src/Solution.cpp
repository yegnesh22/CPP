class Solution {
public:
	int maximumCandies(vector<int>& candies, long long k) {
		int cs = candies.size();
		long long sum = 0;
		long long pcount;
		int minc = 1;
		int maxc = 0;
		int ans = 1;
		int mid;

		for (int i = 0; i < cs; i++) {
			maxc = max(candies[i],maxc);
			sum += candies[i];
		}

		if (sum < k)
			return 0;

		while (minc <= maxc) {
			mid = ((maxc - minc) >> 1) + minc;
			pcount = 0;
			for (int i = 0; i < cs; i++)
				pcount += (candies[i] / mid);
			if (pcount >=  k) {
				ans = mid;
				minc = mid + 1;
			} else
				maxc = mid - 1;
		}

		return ans;
	}
};

