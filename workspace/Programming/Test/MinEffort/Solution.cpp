typedef pair<int, int> cell;

class Solution {
	private:
		priority_queue<int, vector<int>, greater<int>> m_wq;
		const vector<vector<int>> m_adj = {
			{-1,0}, {1,0}, {0,-1}, {0,1},  
		};
		int m_r;
		int m_c;
	public:
		void dfs(cell cur, int maxeff, vector<vector<int>>& m_h, vector<vector<int>>& m_v) {
			if ((cur.first == (m_r - 1)) && (cur.second == (m_c - 1))) {
				m_wq.push(maxeff);
				return;
			} else {
				m_v[cur.first][cur.second] = maxeff;
				for (int i = 0; i < 4; i++) {
					int nr = cur.first + m_adj[i][0];
					int nc = cur.second + m_adj[i][1];
					cell n(nr, nc);
					if ((nr >= 0 && nr < m_r) && (nc >= 0 && nc < m_c)) {
						int nmeff = max(maxeff, abs(m_h[nr][nc] - m_h[cur.first][cur.second]));
						if (nmeff < m_v[nr][nc])
							dfs(n, nmeff, m_h, m_v);
					}
				}
			}
			return;
		}

		int minimumEffortPath(vector<vector<int>>& heights) {
			vector<vector<int>> m_v (101, vector<int>(101, INT_MAX));
			cell start(0,0);	
			m_r = heights.size();
			m_c = heights[0].size();
			m_v[0][0] = true;
			dfs(start, 0, heights, m_v);
			int ret = m_wq.empty() ? 0: m_wq.top();
			return ret;
		}
};

