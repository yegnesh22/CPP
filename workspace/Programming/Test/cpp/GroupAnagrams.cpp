class Solution {
public:
	void printV(vector<vector<string>> r) {
		for (auto& v: r) {
			cout << "{" << flush;
			for (auto& s: v)
				cout << s << "," << flush;
			cout << "}" << endl;
		}
	}
	vector<vector<string>> groupAnagrams(vector<string>& strs) {
		map<pair<set<char>, int>, vector<string>> pstrs;
		vector<vector<string>> result;

		for (auto& s: strs) {
			set<char> sk;
			int sum = 0;
			for (auto&c : s) {
				sk.insert(c);
				sum += c;
			}
			pair<set<char>, int> k({sk, sum});
			if (pstrs.find(k) != pstrs.end())
				pstrs[k].push_back(s);
			else
				pstrs.insert(pair<pair<set<char>,int>, vector<string>> {k, {s}});
		}

		for (auto it = pstrs.begin(); it != pstrs.end(); it++)
			result.push_back(it->second);

		printV(result);
		return result;
	}
};

